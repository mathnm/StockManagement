package modelo;

public class Venda {

	String nome;
	double vlrUnitC;
	double vlrTotC;
	int qtd;
	double vlrUnitV;
	double vlrTotV;
	String dataC;
	String dataV;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public double getVlrUnitC() {
		return vlrUnitC;
	}
	public void setVlrUnitC(double vlrUnitC) {
		this.vlrUnitC = vlrUnitC;
	}
	public double getVlrTotC() {
		return vlrTotC;
	}
	public void setVlrTotC(double vlrTotC) {
		this.vlrTotC = vlrTotC;
	}
	public int getQtd() {
		return qtd;
	}
	public void setQtd(int qtd) {
		this.qtd = qtd;
	}
	public double getVlrUnitV() {
		return vlrUnitV;
	}
	public void setVlrUnitV(double vlrUnitV) {
		this.vlrUnitV = vlrUnitV;
	}
	public double getVlrTotV() {
		return vlrTotV;
	}
	public void setVlrTotV(double vlrTotV) {
		this.vlrTotV = vlrTotV;
	}
	public String getDataC() {
		return dataC;
	}
	public void setDataC(String dataC) {
		this.dataC = dataC;
	}
	public String getDataV() {
		return dataV;
	}
	public void setDataV(String dataV) {
		this.dataV = dataV;
	}
	
	
	
	
	
}
